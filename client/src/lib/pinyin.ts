/**
 * 获取单个汉字的拼音首字母
 */
function getCharPinyin(char: string): string {
  return PINYIN_MAP[char] || char.toLowerCase();
}

/**
 * 获取字符串的拼音首字母串
 * 例如："中山灯具制造商" → "zsdgzzs"
 */
export function getPinyinInitials(text: string): string {
  return text.split('').map(char => {
    if (/[\u4e00-\u9fa5]/.test(char)) {
      return getCharPinyin(char);
    }
    return char.toLowerCase();
  }).join('');
}

/**
 * 检查文本是否匹配搜索词（支持中文全名、拼音首字母）
 * @param text 被搜索的文本
 * @param query 搜索词
 */
export function matchesPinyin(text: string, query: string): boolean {
  if (!query || !text) return true;
  const q = query.toLowerCase().trim();
  const t = text.toLowerCase();
  
  // 1. 直接包含匹配（中文全名、英文）
  if (t.includes(q)) return true;
  
  // 2. 拼音首字母匹配
  const initials = getPinyinInitials(text);
  if (initials.includes(q)) return true;
  
  return false;
}
